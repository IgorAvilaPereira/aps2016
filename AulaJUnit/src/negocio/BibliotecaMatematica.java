public class BibliotecaMatematica {

    public int multiplicacao(int a, int b){
        return a*b;
    }
    
    public float divisao (int a, int b){
        return ((float) a / (float) b);
    }
    
    public int soma(int a, int b){
        return a+b;
    }
    
    public int subtracao(int a, int b){
        return a-b;
    }
            
}
